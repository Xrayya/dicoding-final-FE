class Book {
  constructor(timestamp, title, author, year, isComplete, publisher, category, page, language) {
    this.timestamp = timestamp;
    this.title = title;
    this.author = author;
    this.year = year;
    this.isComplete = isComplete;
    this.publisher = publisher;
    this.category = category;
    this.page = page;
    this.language = language;
  }
}
